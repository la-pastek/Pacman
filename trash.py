# Taille de chaque "pixel" dans la carte
pixel_size = 25


# Fonction pour créer le pixel map à partir de la game map
def create_pixel_map(game_map, pixel_size=25):
    rows = len(game_map)
    cols = len(game_map[0]) if rows > 0 else 0
    pixel_map = []

    for i in range(rows):
        pixel_row = []
        for j in range(cols):
            value = 1 if game_map[i][j] == 1 else 0
            pixel_block = [[value] * pixel_size for _ in range(pixel_size)]
            pixel_row.append(pixel_block)
        pixel_map.append(pixel_row)

    return pixel_map


# Fonction pour vérifier les collisions
def check_collision(x, y, pixel_map):
    # Calcul des indices de la carte en fonction de la position
    map_x = x // pixel_size
    map_y = y // pixel_size

    # Vérifier si les coordonnées sont dans les limites du pixel_map
    if 0 <= map_x < len(pixel_map[0]) and 0 <= map_y < len(pixel_map):
        # Vérifier la valeur du pixel dans le tableau 2D
        return pixel_map[map_y][map_x][0][0] == 1  # Renvoie True si c'est un mur (1)

    # Si hors limites, considérer comme collision
    return True


